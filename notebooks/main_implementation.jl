# ╔═╡ f0c26f77-b6fe-43c5-806b-18b680ec7e0e
function extract_plot_coords(pa::PolyArea)
	v = map(coordinates, pa.outer.vertices)
	return first.(v), last.(v)
end

# ╔═╡ 8a175bb5-20c2-4b4f-bf09-0f3d21c6c8b1
function extract_plot_coords(md::Union{Multi, Domain})
	x = Float64[]
	y = Float64[]
	for i ∈ md.items
		tx, ty = extract_plot_coords(i)
		append!(x, tx, [NaN])
		append!(y, ty, [NaN])
	end
	x,y
end

# ╔═╡ 14091a0b-f5a2-4932-93d1-aec077ae68f5
function PlotlyBase.scattergeo(p::Union{Multi, Domain, Polygon}; kwargs...)
	lon, lat = extract_plot_coords(p)
	scattergeo(; lat, lon, mode="lines", kwargs...)
end

